def generate_verilog_char(hex_lines):
    hex_lines = hex_lines.strip().splitlines()
    result = []
    for i, line in enumerate(hex_lines):
        if i%2 == 0:
            clean_line = line.strip().replace(":","")
        else:
            clean_line+= line.strip().replace(":","")
            num = i // 2
            if num % 3 == 0:
                result.append(f"char[{num // 3}] <= 256'h{clean_line};")
            elif num % 3 == 1:
                result.append(f"char[{num // 3 + 64}] <= 256'h{clean_line};")
            else:
                result.append(f"char[{num // 3 + 128}] <= 256'h{clean_line};")
    return result

